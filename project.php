		<p style="text-align:justify">The ever-increasing demand on the Internet is because it is transformed from a special purpose network to a common platform for many online services such as online transactions, entertainment and for other e-commerce applications. Internet suffers from slow convergence of routing protocols after a network failure. The central goal in the Internet is the ability to recover from failures. Generally in IP networks, when a node/link failure occurs, the IGP routing protocols like OSPF are used to update the forwarding information based on the changed topology and the updated information is distributed to all routers in the network domain and each router individually calculates new valid routing tables. This network-wide IP reconvergence is a time consuming process, and a link or node failure is typically followed by a period of routing instability, which can result into dropping of packets due to invalid routes. This phenomenon has been studied in both IGP and BGP context , and has adverse effects on real-time applications. Many attempts have been made for optimizing the different steps of the convergence of IP routing, i.e., detection, dissemination of information and shortest path calculation. But still the convergence time is still too large for applications with real time demands. Moreover, the IGP convergence process is slow because it is reactive and global, i.e., it reacts to a failure after it has occurred, and it involves all the routers in the domain. In this project we present a scheme for handling single link and node failures in IP networks. Multiple Routing Configurations (MRC) is a proactive and local. This protection mechanism allows recovery in the range of milliseconds.</p>
			
